/**
 * Reserved Module
 * Системные ключи: диапазоны и специальные О.К.
 */

module.exports = {
  // ========================================
  // Системный резерв: ::0:: – ::33::
  // Только для Орденов/Отделов/Админов
  // ========================================
  SYSTEM_RESERVE: (function() {
    const reserve = new Set();
    for (let i = 0; i <= 33; i++) {
      reserve.add(String(i));
      reserve.add(String(i).padStart(2, '0'));
    }
    return reserve;
  })(),

  // ========================================
  // Проверка: в системном резерве?
  // ========================================
  isSystemReserve: function(content) {
    return this.SYSTEM_RESERVE.has(content);
  },

  // ========================================
  // Системные субъекты
  // ========================================
  SYSTEM_SUBJECTS: {
    // ОРАКУЛ-С — представитель НОД-платформы
    // Отправляет У.М. при мутации У.З.
    ORACLE: '::О::'
  },

  // ========================================
  // Зарезервированные префиксы
  // ========================================
  PREFIXES: {
    // Временный О.К. (после инертности)
    GUEST_PREFIX: 'вр.',
    
    // Архивный
    ARCHIVED_PREFIX: 'арх.'
  },

  // ========================================
  // Проверка: может ли О.К. быть админом
  // ========================================
  canBeAdmin: function(okKey) {
    if (!okKey || !okKey.startsWith('::') || !okKey.endsWith('::')) {
      return false;
    }
    const content = okKey.slice(2, -2);
    return this.isSystemReserve(content);
  },

  // ========================================
  // Проверка: системный субъект?
  // ========================================
  isSystemSubject: function(okKey) {
    return okKey === this.SYSTEM_SUBJECTS.ORACLE;
  }
};