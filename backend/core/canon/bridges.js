/**
 * Bridges Module
 * Bridge-symbols — символы, разрешённые для связывания алфавитов
 */

module.exports = {
  // ========================================
  // Bridge-symbols
  // ========================================
  // Д/D — мост между кириллицей и латиницей
  // Разрешён потому что общие буквы (А,В,Е,К,М,Н,О,Р,С,Т,Х) 
  // существуют в двух мирах, и bridge подчёркивает это единство
  BRIDGE_D: ['Д', 'D'],

  // ========================================
  // Проверка: bridge-symbol?
  // ========================================
  isBridge: function(char) {
    return this.BRIDGE_D.includes(char);
  },

  // ========================================
  // Проверка: весь контент bridge-only?
  // ========================================
  isBridgeOnly: function(content) {
    if (!content) return false;
    const chars = [...content];
    return chars.length > 0 && chars.every(c => this.isBridge(c));
  },

  // ========================================
  // Список всех bridge-символов
  // ========================================
  ALL: this.BRIDGE_D
};